package _1000;

import java.util.Scanner;

public class _1150_2 {

	public static void main(String[] args) {
		// 1. 정수 2개 입력
		Scanner sc = new Scanner(System.in);
		int a = sc.nextInt();
		int b = sc.nextInt();
		// 2. (조건) ? 참 : 거짓;
	System.out.println((a==b ? 1 : 0));
	sc.close();
	// a > b
	// a < b
	// a >= b
	// a == b
	}
}
